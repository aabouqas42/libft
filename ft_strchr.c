#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	p = (char *)s;
	if (c >= 0 && c <= 126)
	{
		while (*p && *p != c)
			p++;
		if (*p == c)
			return (p);
	}
	return (0);
}
